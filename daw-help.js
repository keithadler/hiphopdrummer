/**
 * Generate the SONG ARRANGEMENT section for help files.
 * Lists each section with its bar count so users know the structure.
 * @returns {string[]} Array of lines for the arrangement section
 */
function _arrangementSection() {
  var lines = [
    'SONG ARRANGEMENT',
    '----------------',
    'This beat has the following structure. Use this to set up your',
    'sequences, patterns, or regions in the correct order and length.',
    ''
  ];
  var totalBars = 0;
  if (typeof arrangement !== 'undefined' && arrangement.length > 0) {
    for (var i = 0; i < arrangement.length; i++) {
      var sec = arrangement[i];
      var bars = Math.ceil(((typeof secSteps !== 'undefined' && secSteps[sec]) || 32) / 16);
      totalBars += bars;
      var label = (typeof SL !== 'undefined' && SL[sec]) ? SL[sec] : sec;
      lines.push('  ' + (i + 1) + '. ' + label + ' — ' + bars + ' bar' + (bars > 1 ? 's' : ''));
    }
    lines.push('');
    lines.push('  Total: ' + totalBars + ' bars');
  }
  lines.push('');
  return lines;
}

