/**
 * Generate the swing status line for help file headers.
 * @param {number} s - Swing percentage
 * @param {boolean} noSwing - True if swing was NOT baked into MIDI
 * @returns {string} Status text for the header
 */
function _swingStatus(s, noSwing) {
  return noSwing
    ? 'Swing: ' + s + '% (NOT baked in — notes are on a straight grid. Add ' + s + '% swing in your DAW.)'
    : 'Swing: ' + s + '% (already baked in — do not add more)';
}

/**
 * Generate the SWING NOTE section for DAW help files.
 * @param {string} dawName - DAW name for context
 * @param {number} s - Swing percentage
 * @param {boolean} noSwing - True if swing was NOT baked into MIDI
 * @returns {string[]} Array of lines for the swing section
 */
function _swingNote(dawName, s, noSwing) {
  if (noSwing) {
    return [
      'SWING NOTE',
      '----------',
      'These MIDI files are on a STRAIGHT GRID — swing is NOT baked in.',
      'To match the original beat, set your ' + dawName + ' swing/groove to ' + s + '%',
      'at 1/16 note resolution. This gives you full control to adjust the feel.',
    ];
  }
  return [
    'SWING NOTE',
    '----------',
    'The MIDI files have ' + s + '% swing baked into the note timing.',
    'Do NOT add additional swing/groove in ' + dawName + ' — it will',
    'double the timing offset and make the beat sound sloppy.',
    'Do NOT quantize on import — it will destroy the swing timing.',
  ];
}

